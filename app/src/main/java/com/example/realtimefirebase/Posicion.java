package com.example.realtimefirebase;

public class Posicion {
    float x;
    float y;
    float z;

    public Posicion(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }
}
